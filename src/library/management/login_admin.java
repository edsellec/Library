    private void Admin_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Admin_loginActionPerformed
        String Library = "C:\\Library\\Users\\History";
        File file = new File(Library);
        if (!file.exists()) {
            try {
                boolean success = (new File(Library)).mkdirs();
                if (success) {
                    System.out.println("Directories: " + Library + " created");
                } 
            } catch (Exception e) {//Catch exception if any
                System.err.println("Error: " + e.getMessage());
            }
        }
        
        String id = Admin_key.getText();
        String pass = Admin_pass.getText();
        if (id.equals("admin") && pass.equals("admin")){
            admin_menu.start();
            dispose();
            Index text = new Index();
            try {
                text.logHistory("Administrator has logged in");
            } catch (IOException ex) {
                Logger.getLogger(login_admin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else {
            ErrorManager.catchError("Username or Password is incorrect!");
        }
    }//GEN-LAST:event_Admin_loginActionPerformed

