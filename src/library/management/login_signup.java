    private void Signup_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Signup_submitActionPerformed
        String Library = "C:\\Library\\Users\\History";
        File file = new File(Library);
        if (!file.exists()) {
            try {
                boolean success = (new File(Library)).mkdirs();
                if (success) {
                    System.out.println("Directories: " + Library + " created");
                } 
            } catch (Exception e) {//Catch exception if any
                System.err.println("Error: " + e.getMessage());
            }
        }
        
        String id = Signup_id.getText();
        String pass = Signup_password.getText();
        String confirm = Signup_confirmPass.getText();
        String idCut = "";
        int lengthId = id.length();
        if(lengthId > 3) idCut = id.substring(0,3);
        if ((idCut.equals("201")) && (lengthId == 10) && (!"".equals(id)) && (!"".equals(pass)) && (confirm.equals(pass))) {
            try {
                Index text = new Index();
                text.signup(id,pass);
                login_success.start();
                text.logHistory(id+"has signed up");
                dispose();
            } catch (IOException ex) {
                Logger.getLogger(login_signup.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else if(("".equals(id)) || ("".equals(pass)) || ("".equals(confirm))){
            ErrorManager.catchError("All textfields must contain information!");
        }
        else if((lengthId != 10) || (!idCut.equals("201"))){
            ErrorManager.catchError("Valid ID number only!");
        }
        else if(!confirm.equals(pass)){
            ErrorManager.catchError("Password isn't matched!");
        }
    }//GEN-LAST:event_Signup_submitActionPerformed

