    private void admin_viewHistoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_admin_viewHistoryActionPerformed
        String idLog = admin_searchId.getText();
        String user = idLog+"_history.txt";
        String USER_FILE_NAME = "C:\\Library\\Users\\History\\"+user;
        File temp = new File(USER_FILE_NAME);
        if (!idLog.equals("")) {
            if (!temp.exists()){
                ErrorManager.catchError(idLog+"'s History doesn't exist!");
            }
            else {
                admin_history.start();
                admin_history.User_showId.setText(idLog);
                admin_history.idLog = idLog;
                dispose();
            }
        }
        else {
            ErrorManager.catchError("Search field must contain information!");
        }
    }//GEN-LAST:event_admin_viewHistoryActionPerformed

